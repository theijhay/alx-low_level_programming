int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtraction
 * @a: num1
 * @b: num2
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication
 * @a: num1
 * @b: num2
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division
 * @a: num1
 * @b: num2
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus
 * @a: num1
 * @b: num2
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
